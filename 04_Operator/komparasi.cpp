// == : artinya sama dengan
// > : artinya lebih besar
// < : artinya lebih kecil
// >= : lebih besar atau sama
// <= : lebih kecil atau sama

// kalo hasilnya 1 berarti TRUE
// kalo hasilnya 0 berarti FALSE

int main()
{
    int x = 10;
    int y = 12;

    cout<<"___CONTOH DENGAN (==)___"<<endl;
    int hasil1 = x == y;
    cout<<"Hasil dari x == y: "<<hasil1<<endl<<endl;

    cout<<"___CONTOH DENGAN (>)___"<<endl;
    int hasil2 = x > y;
    cout<<"Hasil dari x > y: "<<hasil2<<endl<<endl;

    cout<<"___CONTOH DENGAN (<)___"<<endl;
    int hasil3 = x < y;
    cout<<"Hasil dari x < y: "<<hasil3<<endl<<endl;
    return 0;

    // SISANYA GA MUNGKIN LO GATAU >;( 
}